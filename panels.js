  function generatePanelHTMLCode(link, name, imageLink)
  {
    var HTMLCode = `
    <a class="panel" href="` + link + `/">
      <div class="panel">
        <div class="panelText">` + 
          name +
       `</div>
        <div class="panelImg">
          <img class="panel" src="` + imageLink + `">` +
       `</div>
     </div>
    </a>
    `
    
    return HTMLCode;

  }
  
  function generatePanels()
  {
    var htmlCode = "";
    for (var i in links)
    {
      htmlCode += generatePanelHTMLCode(
        links[i][0], links[i][1], links[i][0]+'/main_thumbnail.jpg');
    }
    $("#panels").html(htmlCode);
  }

  function updateCSS()
  {
    var width = $(window).width();
    if (width < 400)
    {
      $("div.panel").css('width', "45%");
    }
    else
    {
      $("div.panel").css('width', "170px");
    }
  }

  $( document ).ready(function() 
  { 
    generatePanels();
    updateCSS();
  }
  );
  
    $( window ).resize(function() 
  {
    updateCSS();
  }
  );
